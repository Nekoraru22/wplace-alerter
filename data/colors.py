# Lista en el orden exacto
COLOR_LIST = list(Color)

def get_color_id(rgb):
    rgb_tuple = tuple(rgb)
    for idx, color in enumerate(COLOR_LIST):
        if color.value == rgb_tuple:
            return color.name, idx
    return None, None

# Ejemplo
